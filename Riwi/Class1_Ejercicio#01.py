

"""1. MAYOR DE EDAD """
"""Pide al usuario su edad, si tiene 18 o mas, imprime "eres mayor de edad" si no imprime "eres menor de edad" """

print("por favor ingresa tu edad"); #imprime un mensaje

edad = int(input()); # le pedimos al usuario que ingrese su edad en un tipo de dato entero

if edad >=18:{ # validamos que la edad ingresada por el usuario sea mayor o igual a 18
    
    print ("eres mayor de edad") # se imprime un mensaje
    
}    
else:{ # esto es si no fuera mayor a 18
    
    print ("eres menor de edad") # se imprime un mensaje
} 









    
    
    
    


    
    
 